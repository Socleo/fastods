    symbol text,
    subcategory text,
    element_name text,
    pt_group integer,
    period decimal,
    atomic_weight decimal,
    density decimal,
    melting_point decimal,
    boiling_point decimal,
    specific_heat_capacity decimal,
    electro_negativity decimal,
    abundance_in_earths_crust decimal
)
